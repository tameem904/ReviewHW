package homeworkReviewClasses;

public class NestedLoopsReview {

    public static void main(String[] args) {

        for (int i = 1; i >=3; i++) {

            System.out.println(i);

            for (int j = 0; j <=1 ; j++) {

                System.out.println(j);
            }
        }
    }
}